CREATE OR REPLACE TRIGGER relikey_status_trigger
AFTER UPDATE ON REPLY_LIKEY
FOR EACH ROW
WHEN (OLD.RE_LISTATUS <> NEW.RE_LISTATUS)
DECLARE
    relikey_increment INTEGER;
BEGIN
    IF :NEW.RE_LISTATUS = 'Y' THEN
        relikey_increment := 1;
    ELSE
        relikey_increment := -1;
    END IF;

